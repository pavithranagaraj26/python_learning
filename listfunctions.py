fruits=["Apple","Mango","peach","orange"]

#append
fruits.append("banana") #add in the last
fruits.append("chico")
print(fruits)

#length of the list
print(len(fruits))

#insert like append but we can add wherever/ index you want

fruits.insert(1,"banana") #adding new banana not overwriting or replacing the old banana

print(fruits)

#indexfunction returns index

print(fruits.index("Apple"))
